void displayMoves(char* piece, const int* board)
{
	std::cout << "   --------";
	int pieceIter = 0;
	while (piece[pieceIter]) {
		std::cout << piece[pieceIter];
		++pieceIter;
	}
	std::cout << "--------\n";
	for (int i = 0; i < 15; ++i) {
		for (int j = 0; j < 15; ++j) {
			std::cout << board[(i*15)+j] << " ";
		}
		std::cout << "\n";
	}
	std::cout << "\n";
}

